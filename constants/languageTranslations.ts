export type TranslationKey = keyof typeof TRANSLATIONS['en'];

export function t(code: string, key: TranslationKey): string {
  const lang = TRANSLATIONS[code as keyof typeof TRANSLATIONS];
  if (!lang) return TRANSLATIONS.en[key];
  return (lang as any)[key] ?? TRANSLATIONS.en[key];
}

export const RTL_LANGS = new Set(['ar', 'fa']);
export const isRTL = (code: string) => RTL_LANGS.has(code);
